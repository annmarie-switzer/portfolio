function randomWorkout(obj) {
	arr = Object.keys(obj);
	return arr[Math.floor(Math.random() * arr.length)];
}

function getMoves(obj, workout) {
	return obj[workout];
}

function generateWorkout(obj) {
	var workout = randomWorkout(obj);
	var moves = getMoves(obj, workout);
	var set = 0;

	$("#workout").text(workout);

	for (i=0; i<moves.length; i++) {
		$("#moves").append("<div id=set-" + set + "></div>");
		if (moves[i] !== 'repeat that!') {
			$("#set-"+set).append("<div class='form-check'><label class='custom-control custom-checkbox'><input class='custom-control-input' type='checkbox' id='indicator-" + set + "'><span class='custom-control-indicator'></span><span class='custom-control-description text-capitalize'>" + moves[i] + "</span></label></div>");
		}
		else {
			$("#set-"+set).append("<button class='custom-control-description text-capitalize btn btn-outline-primary mb-3 repeat' id=repeat-" + set + ">" + moves[i] + "</button>")
			set += 1;
		}
	}

	$(".repeat").on("click", function () {
		var setId = $(this).attr('id');
		var setNum = setId.substr(setId.length - 1);
		$("#set-"+setNum).find(":checked").prop('checked', false);
		$("#repeat-"+setNum).attr("disabled", true);
	});
}

